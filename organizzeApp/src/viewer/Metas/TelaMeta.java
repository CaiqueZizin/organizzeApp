    public  void attMetasNaTelaSalario(){
        String sql = "SELECT *  FROM `metas` WHERE Categoria = 'Salario'  ; " ;
        try{
            DAO dao = new DAO();
            Connection conn = dao.conectar();
            PreparedStatement ps = conn.prepareStatement(sql);
            Statement st = conn.createStatement();;
            ResultSet rs = st.executeQuery(sql);     
            double resul = 0;
            double res = 0;
            while(rs.next()){
                tbProgressoSalario.setText(rs.getDouble("Progresso") + "");
                tbMetaSalario.setText(rs.getDouble("Meta") + "");
                barraProgressoSalario.setMaximum((int) rs.getDouble("Meta"));
                barraProgressoSalario.setValue((int) rs.getDouble("Progresso"));               
            }
        }catch(SQLException sqle){
            sqle.printStackTrace();
        }  
    }
    
