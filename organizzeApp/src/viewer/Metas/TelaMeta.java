    private void btSalvarMetasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarMetasActionPerformed
        double mtSalario;
        double mtEmprestimos;
        double mtInvestimentos;
        double mtOutros;
        //Aqui vamos ver se tem alguns campos vazios , se tiver vamos setar 0 como padrão        
        if(tbMetaSalario.getText().isEmpty()){tbMetaSalario.setText("0");};
        if(tbMetaEmprestimos.getText().isEmpty()){tbMetaEmprestimos.setText("0");};
        if(tbMetaInvestimentos.getText().isEmpty()){tbMetaInvestimentos.setText("0");};
        if(tbMetaOutrasReceitas.getText().isEmpty()){tbMetaOutrasReceitas.setText("0");};
        
        mtSalario = Double.parseDouble(tbMetaSalario.getText());
        mtEmprestimos = Double.parseDouble(tbMetaEmprestimos.getText());
        mtInvestimentos = Double.parseDouble(tbMetaInvestimentos.getText());
        mtOutros = Double.parseDouble(tbMetaOutrasReceitas.getText());
       
        //Agora chamamos os métodos que irão fazer a inserção das metas
        salvarMetaSalario(mtSalario);
        salvarMetaEmprestimo(mtEmprestimos);
        salvarMetaInvestimentos(mtInvestimentos);
        salvarMetaOutros(mtOutros);
        //e chamamos os metodos que atualizar a tela
        attMetasNaTelaSalario();
        attMetasNaTelaEmprestimos();
        attMetasNaTelaInvestimentos();
        attMetasNaTelaOutros();
        //Vamos mandar a mensagem de metas salvas
        JOptionPane.showMessageDialog(null,"Metas Salvas com sucesso");
        
    }//GEN-LAST:event_btSalvarMetasActionPerformed

